package com.kia;

public class calculator {
	
	public int add(int a, int b)
	{
		int addition = a + b;
		return addition;
	}
	
	public int sub(int a, int b)
	{
		int subtract = a - b;
		return subtract;
	}
	
	public int mul(int a, int b)
	{
		int multiply = a * b;
		return multiply;
	}
	
	public int div(int a, int b)
	{
		int divide = a / b;
		return divide;
	}
	
	

}
